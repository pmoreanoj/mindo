                <h1>Sobre Activa Mindo 2013</h1>
                    <p>El proyecto de Activa Mindo 2013 nace como una iniciativa de una de las ramas de la
                    plataforma EcoHelix. Esta se basa en el turismo responsable y en buscar a los jóvenes
                    emprendedores como agentes de cambio en los distintos sitios donde se enfoca la plataforma.
                    Uno de esos sitios es Mindo, Pichincha - Ecuador.</p>
                    <p>Entre nuestros objetivos por lograr se encuentran:</p>
                    Objetivos
                    <ul>
                        <li>Movilizar jóvenes como agentes de cambio</li>
			<li>Generar liderazgo</li>
			<li>Usar recursos y talentos locales</li>
                        <li>Generar competitividad y calidad</li>
                    </ul>
                    <p>Se ha trabajado todo este semestre en el proyecto Activa Mindo 2013. Junto con el liderazgo
                    de PhD. David Lansdale, profesor de la Universidad San Francisco de Quito, su clase de 
                    Principles of Management se comprometió a trabajar arduamente para alcanzar la realización
                    del proyecto. Básicamente el proyecto se divide en algunos temas:</p>
		<ol>
                    <li>Creación de las historias orales por parte de jóvenes y adultos mayores en Mindo.</li>
                    <li>Crear una base de datos informativa sobre los distintos negocios de Mindo.</li>
                    <li>Promover el cambio en Mindo y buscar gente que se haga cargo del proyecto en el futuro.</li>
                    <li>Unir a la comunidad de Mindo y ayudar a que puedan explotar su potencial turístico.</li>
		</ol>
		<p>Trabajar desde adentro con Mindo ha sido una experiencia maravillosa para todos los que formamos
                este grupo de trabajo. Le tenemos mucha fé a Mindo y es por lo mismo que hemos puesto mucho esfuerzo
                para cumplir todos los objetivos que nos planteamos en un principio. Sabemos que no es fácil lograr
                el cambio, pero si todos juntos: comunidad, turistas, emprendedores, gobierno, sector privado se
                unen; podemos lograr una diferencia. Activa Mindo 2013 es apenas una semilla de todo lo que queremos
                sembrar en Mindo.</p>
                <p>Principles of Management Class - 2013</p>
                <p>Universidad San Francisco de Quito, Quito - Ecuador.</p>
